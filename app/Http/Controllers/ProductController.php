    /**
     * POST BRE(A)D - Store data.
     *
     * @param \Illuminate\Http\Request $request
     *
     * @return \Illuminate\Http\RedirectResponse
     */
    public function store(Request $request)
    {
        $slug = $this->getSlug($request);

        $dataType = Voyager::model('DataType')->where('slug', '=', $slug)->first();

        // Check permission
        $this->authorize('add', app($dataType->model_name));

        // Validate fields with ajax
        $val = $this->validateBread($request->all(), $dataType->addRows);

        if ($val->fails()) {
            return redirect()->back()->withInput($request->all())->withErrors($val->errors());
        }

        if (!$request->has('_validate')) {
            if($request->type != 'bundle')
                $request->merge([
                    'featured' => null
                ]);
            $data = $this->insertUpdateData($request, $slug, $dataType->addRows, new $dataType->model_name());

            if ($request->type == 'bundle' && isset($request->bundle_products) && count($request->bundle_products) > 0) {
                $bundle_product = array();
                foreach ($request->bundle_products as $productId) {
                    $bundle_product[] = [
                        'bundle_id' => $data->id,
                        'product_id' => $productId,
                    ];
                }
                BundleProduct::insert($bundle_product);
            }

            if (isset($request->products) && count($request->products) > 0) {
                $productSuggestData = array();
                foreach ($request->products as $productId) {
                    $productSuggestData[] = [
                        'product_id' => $data->id,
                        'suggest_product_id' => $productId,
                    ];
                }
                SuggestProduct::insert($productSuggestData);
            }

            event(new BreadDataAdded($dataType, $data));

            if ($request->ajax()) {
                return response()->json(['success' => true, 'data' => $data]);
            }

            return redirect()
                ->route("voyager.{$dataType->slug}.index", ['type' => $request->filter_type])
                ->with([
                    'message' => __('voyager::generic.successfully_added_new') . " {$dataType->display_name_singular}",
                    'alert-type' => 'success',
                ]);
        }
    }

