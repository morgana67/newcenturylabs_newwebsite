    // POST BR(E)AD
    public function update(Request $request, $id)
    {
        $slug = $this->getSlug($request);

        $dataType = Voyager::model('DataType')->where('slug', '=', $slug)->first();

        // Compatibility with Model binding.
        $id = $id instanceof Model ? $id->{$id->getKeyName()} : $id;

        $data = call_user_func([$dataType->model_name, 'findOrFail'], $id);

        // Check permission
        $this->authorize('edit', $data);

        // Validate fields with ajax
        $val = $this->validateBread($request->all(), $dataType->editRows, $dataType->name, $id);

        if ($val->fails()) {
            return redirect()->back()->withInput($request->all())->withErrors($val->errors());
        }

        if (!$request->ajax()) {
            if($request->type != 'bundle')
                $request->merge([
                    'featured' => null
                ]);
            $this->insertUpdateData($request, $slug, $dataType->editRows, $data);
            BundleProduct::where('bundle_id',$id)->delete();
            SuggestProduct::where('product_id',$id)->delete();

            if ($request->type == 'bundle' && isset($request->bundle_products) && count($request->bundle_products) > 0) {
                $bundle_product = array();
                foreach ($request->bundle_products as $productId) {
                    $bundle_product[] = [
                        'bundle_id' => $data->id,
                        'product_id' => $productId,
                    ];
                }
                BundleProduct::insert($bundle_product);
            }

            if (isset($request->products) && count($request->products) > 0){
                $productSuggestData = array();
                foreach ($request->products as $productId){
                    $productSuggestData[] = [
                        'product_id' => $data->id,
                        'suggest_product_id' => $productId,
                    ];
                }
                SuggestProduct::insert($productSuggestData);
            }

            event(new BreadDataUpdated($dataType, $data));

            return redirect()
                ->route("voyager.{$dataType->slug}.index", ['type' => $request->filter_type])
                ->with([
                    'message'    => __('voyager::generic.successfully_updated')." {$dataType->display_name_singular}",
                    'alert-type' => 'success',
                ]);
        }
    }

